module comparator_4_bit(
input [3:0]a,b,
output l,e,g);
assign l = (~a[3]&b[3])|
	    (~(a[3]^b[3])&~a[2]&b[2])|
	    (~(a[3]^b[3])&~(a[2]^b[2])&~a[1]&b[1])|
	    (~(a[3]^b[3])&~(a[2]^b[2])&~(a[1]^b[1])&~a[0]&b[0]);

assign e = ~(a[3]^b[3])&~(a[2]^b[2])&~(a[1]^b[1])&~(a[0]^b[0]);

assign g = (a[3]&~b[3])|
	    (~(a[3]^b[3])&a[2]&~b[2])|
	    (~(a[3]^b[3])&~(a[2]^b[2])&a[1]&~b[1])|
	    (~(a[3]^b[3])&~(a[2]^b[2])&~(a[1]^b[1])&a[0]&~b[0]);
endmodule
